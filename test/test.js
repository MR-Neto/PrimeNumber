const assert = require('assert');
const firstPrimeNumber = require('../prime');

describe('First Prime Number', function () {
  it('should return -1 when the value is not present', function () {
    assert.equal(firstPrimeNumber("277"), "277");
  });
  it('should return -1 when the value is not present', function () {
    assert.equal(firstPrimeNumber("277"), "277");
  });
  it('should return -1 when the value is not present', function () {
    assert.equal(firstPrimeNumber("277"), "277");
  });
  it('should return -1 when the value is not present', function () {
    assert.equal(firstPrimeNumber("277"), "277");
  });
});